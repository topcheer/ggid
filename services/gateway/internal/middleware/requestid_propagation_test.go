func TestPropagateRequestID_RoundTrip(t *testing.T) {
	var capturedID string
	handler := PropagateRequestID(http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		capturedID = RequestIDFromContext(r.Context())

		// Simulate propagating to a downstream HTTP request
		downstreamReq := httptest.NewRequest("GET", "/downstream", nil)
		InjectRequestIDHeader(r.Context(), downstreamReq)

		if downstreamReq.Header.Get("X-Request-ID") != capturedID {
			t.Errorf("downstream X-Request-ID = %q, want %q",
				downstreamReq.Header.Get("X-Request-ID"), capturedID)
		}

		// Simulate propagating to gRPC
		grpcCtx := RequestIDToOutgoingContext(r.Context())
		md, _ := metadata.FromOutgoingContext(grpcCtx)
		if vals := md.Get(GRPCRequestIDKey); len(vals) == 0 || vals[0] != capturedID {
			t.Errorf("gRPC metadata missing request ID")
		}

		w.WriteHeader(200)
	}))

	req := httptest.NewRequest("GET", "/api/v1/data", nil)
	req.Header.Set("X-Request-ID", "round-trip-id")
	w := httptest.NewRecorder()
	handler.ServeHTTP(w, req)

	if capturedID != "round-trip-id" {
		t.Errorf("captured ID = %q, want 'round-trip-id'", capturedID)
	}
}
