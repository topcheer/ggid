func TestRequestIDFromContext(t *testing.T) {
	ctx := ContextWithRequestID(context.Background(), "req-123")
	if got := RequestIDFromContext(ctx); got != "req-123" {
		t.Errorf("RequestIDFromContext = %q, want 'req-123'", got)
	}

	// Empty context
	if got := RequestIDFromContext(context.Background()); got != "" {
		t.Errorf("RequestIDFromContext(empty ctx) = %q, want ''", got)
	}
}

func TestPropagateRequestID_FromHeader(t *testing.T) {
	handler := PropagateRequestID(http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		id := RequestIDFromContext(r.Context())
		if id != "client-provided-id" {
			t.Errorf("expected 'client-provided-id', got %q", id)
		}
		w.WriteHeader(200)
	}))

	req := httptest.NewRequest("GET", "/api/v1/data", nil)
	req.Header.Set("X-Request-ID", "client-provided-id")
	w := httptest.NewRecorder()
	handler.ServeHTTP(w, req)

	if w.Header().Get("X-Request-ID") != "client-provided-id" {
		t.Errorf("response X-Request-ID = %q", w.Header().Get("X-Request-ID"))
	}
}

func TestPropagateRequestID_AutoGenerated(t *testing.T) {
	handler := PropagateRequestID(http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		id := RequestIDFromContext(r.Context())
		if id == "" {
			t.Error("expected auto-generated request ID in context")
		}
		w.WriteHeader(200)
	}))

	req := httptest.NewRequest("GET", "/api/v1/data", nil)
	// No X-Request-ID header
	w := httptest.NewRecorder()
	handler.ServeHTTP(w, req)

	id := w.Header().Get("X-Request-ID")
	if id == "" {
		t.Error("expected auto-generated X-Request-ID in response")
	}
}

func TestRequestIDToOutgoingContext(t *testing.T) {
	ctx := ContextWithRequestID(context.Background(), "grpc-req-id")
	outCtx := RequestIDToOutgoingContext(ctx)

	md, ok := metadata.FromOutgoingContext(outCtx)
	if !ok {
		t.Fatal("expected outgoing metadata to be set")
	}

	vals := md.Get(GRPCRequestIDKey)
	if len(vals) != 1 || vals[0] != "grpc-req-id" {
		t.Errorf("metadata[%s] = %v, want ['grpc-req-id']", GRPCRequestIDKey, vals)
	}
}

func TestRequestIDToOutgoingContext_NoID(t *testing.T) {
	// When context has no request ID, should return context unchanged
	ctx := context.Background()
	outCtx := RequestIDToOutgoingContext(ctx)

	// Should not add any metadata
	md, ok := metadata.FromOutgoingContext(outCtx)
	if ok {
		vals := md.Get(GRPCRequestIDKey)
		if len(vals) > 0 {
			t.Errorf("expected no metadata when no request ID, got %v", vals)
		}
	}
}

func TestRequestIDFromIncomingMetadata(t *testing.T) {
	md := metadata.Pairs(GRPCRequestIDKey, "incoming-req-id")
	id := RequestIDFromIncomingMetadata(md)
	if id != "incoming-req-id" {
		t.Errorf("expected 'incoming-req-id', got %q", id)
	}

	// Empty metadata
	id = RequestIDFromIncomingMetadata(metadata.MD{})
	if id != "" {
		t.Errorf("expected empty string, got %q", id)
	}
}

